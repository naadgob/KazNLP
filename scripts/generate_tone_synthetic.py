def build_clear_mixed(positive: bool, domain: str) -> str:
    if domain == "food":
        ru_fn, kz_fn = (ru_pos_food, kz_pos_food) if positive else (ru_neg_food, kz_neg_food)
    elif domain == "services":
        ru_fn, kz_fn = (ru_pos_service, kz_pos_service) if positive else (ru_neg_service, kz_neg_service)
    else:
        ru_fn, kz_fn = (ru_pos_general, kz_pos_general) if positive else (ru_neg_general, kz_neg_general)

    parts = []
    order = RNG.choice([(ru_fn, kz_fn), (kz_fn, ru_fn)])
    parts.append(order[0]())
    parts.append(order[1]())
    if RNG.random() < 0.45:
        extra = kz_fn() if RNG.random() < 0.5 else ru_fn()
        parts.insert(RNG.randint(0, len(parts)), extra)
    if positive and RNG.random() < 0.25:
        name = pick_name()
        parts.append(f"{name} молодец, рахмет көп.")
    unique: list[str] = []
    for p in parts:
        if p not in unique:
            unique.append(p)
    return " ".join(unique)


def _idx(seq: list[str], n: int, salt: int = 0) -> str:
    return seq[(n * 7 + salt * 11) % len(seq)]


def _split_suffix(n: int, positive: bool) -> str:
    """Factual tail for uniqueness — no praise on negative rows."""
    city = CITIES[n % len(CITIES)]
    if positive:
        tails = [
            f" В {city} были на прошлой неделе.",
            f" {city} қаласында болдық біз.",
            f" Кезек {(n % 9) + 3} адамдай болды.",
            f" Счёт около {1100 + (n * 89) % 4000} тенге.",
        ]
    else:
        tails = [
            f" В {city} были на прошлой неделе.",
            f" {city} қаласында болдық біз.",
            f" Кезек {(n % 9) + 3} адамдай болды.",
            f" Счёт около {1100 + (n * 89) % 4000} тенге.",
            f" Шамамен {(n % 40) + 15} минут күттік.",
        ]
    return tails[n % len(tails)]


