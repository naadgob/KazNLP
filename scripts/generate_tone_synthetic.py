def salt(text: str, n: int, positive: bool = True) -> str:
    """Deterministic neutral/context variation; tone-aligned closers only match label."""
    city = CITIES[n % len(CITIES)]
    name = (NAMES_M + NAMES_F)[n % len(NAMES_M + NAMES_F)]
    mins = 12 + (n * 7) % 55
    tenge = 900 + (n * 137) % 8500
    # Factual context only — no praise (reads as positive on negative reviews).
    neutral_ru = [
        f" В {city} заходили в {WEEKDAYS[n % len(WEEKDAYS)]}.",
        f" Ждали примерно {mins} минут.",
        f" Счёт вышел около {tenge} тенге.",
        f" Заказ оформили около {mins + 3} минут.",
    ]
    neutral_kz = [
        f" {city} қаласынан келдік бүгін.",
        f" Шамамен {mins} минут күттік.",
        f" Баға {tenge} теңге шамасында болды.",
        f" Тапсырыс {mins} минутта дайын болды.",
    ]
    if positive:
        tone_ru = [" Вернусь сюда ещё раз точно.", " Рекомендую знакомым."]
        tone_kz = [" Тағы да келеміз деп шештік.", " Ұнады, рахмет көп."]
    else:
        tone_ru = [" Больше не приду сюда.", " Не советую никому."]
        tone_kz = [" Енді келмеймін.", " Ұнамады бізге."]
    pool_ru = neutral_ru + tone_ru
    pool_kz = neutral_kz + tone_kz
    extra = pool_ru[n % len(pool_ru)] if (n // 5) % 2 == 0 else pool_kz[n % len(pool_kz)]
    return text.strip() + extra


def tone_qc(text: str, label: str) -> list[str]:
    issues = list(qc_row(text, "mixed"))
    sig = text_signals(text)
    if not sig["potential_real_mixed"]:
        issues.append("weak_bilingual")
    if label not in ("positive", "negative"):
        issues.append("bad_label")
    return issues


def has_code_switch(text: str) -> bool:
    parts = [p.strip() for p in re.split(r"[.!?]+", text) if len(p.strip()) >= 4]
    from precision_filter import has_kazakh_signal, has_russian_signal

    ru_parts = sum(
        1 for p in parts if has_russian_signal(p) and not has_kazakh_signal(p)
    )
    kz_parts = sum(
        1 for p in parts if has_kazakh_signal(p) and not has_russian_signal(p)
    )
    return ru_parts >= 1 and kz_parts >= 1


