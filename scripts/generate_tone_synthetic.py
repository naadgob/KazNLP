    with OUT_SYNTH.open("w", encoding="utf-8-sig", newline="") as f:
        w = csv.DictWriter(
            f,
            fieldnames=["text", "language", "label", "label_source", "seed_id", "batch"],
        )
        w.writeheader()
        w.writerows(rows)
    print(f"Wrote {OUT_SYNTH.name}: {len(rows)} rows")


